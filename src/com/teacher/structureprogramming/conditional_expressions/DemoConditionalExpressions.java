package com.teacher.structureprogramming.conditional_expressions;

public class DemoConditionalExpressions {

	public static void main(String[] args) {
		// Demo về biểu thức điều kiện thay thế cho if ... else
		int a = 10;
		int b = 3000;

		int max = (a > b) ? a : b;
		System.out.println(max);
	}

}
